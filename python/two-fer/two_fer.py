"""Functions for implementing two-fer exercism exercise"""

def two_fer(name="you"):
    """ Return a string with the name of the person to whom the two-fer is being
        made. If no name is given, the string will be "One for you, one for me."
        :param name: optional. The name of the person to whom the two-fer is being made.
    """
    return f"One for {name}, one for me."
